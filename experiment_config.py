def _build_test_modes(patterns):
    modes = []
    for pattern_name, pattern_modes in patterns.items():
        for mode_name in ("mode10", "mode8", "mode6", "mode4"):
            modes.append(
                {
                    "name": f"{pattern_name}_{mode_name}",
                    "pattern": pattern_name,
                    "mode": mode_name,
                    "electrodes": pattern_modes[mode_name],
                }
            )
    return modes


def _validate_pattern_config():
    if len(stim_electrodes) > max_stim_electrodes:
        raise ValueError(
            f"stim_electrodes must contain at most {max_stim_electrodes} electrodes."
        )

    for pattern_name, pattern_modes in stim_patterns.items():
        if "mode10" not in pattern_modes:
            raise ValueError(f"{pattern_name} must define mode10.")
        if len(pattern_modes["mode10"]) != 10:
            raise ValueError(f"{pattern_name}.mode10 must contain exactly 10 electrodes.")
        for mode_name in ("mode10", "mode8", "mode6", "mode4"):
            electrodes = pattern_modes.get(mode_name)
            if not electrodes:
                raise ValueError(
                    f"{pattern_name}.{mode_name} must contain at least one electrode."
                )


